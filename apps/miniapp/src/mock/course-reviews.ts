export const DETAIL_REVIEW_PREVIEW = 2

export function getCourseReviewsByKey(courseKey: string, count: number): CourseReview[] {
  const snippets = REVIEW_SNIPPETS[courseKey]
  if (!snippets) return []
  return buildReviews(count, snippets)
}

export function getCourseReviewPreview(courseKey: string, count: number) {
  return getCourseReviewsByKey(courseKey, count).slice(0, DETAIL_REVIEW_PREVIEW)
}
