function buildReviews(count: number, snippets: string[]): CourseReview[] {
  return Array.from({ length: count }, (_, index) => ({
    id: `review_${index + 1}`,
    userName: REVIEW_NAMES[index % REVIEW_NAMES.length],
    rating: index % 3 === 0 ? 5 : index % 3 === 1 ? 4.5 : 4,
    content: snippets[index % snippets.length],
    createdAt: new Date(Date.now() - index * 86400000 * 4).toISOString().slice(0, 10),
  }))
}

